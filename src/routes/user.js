import express from "express";
import { registerUser, getAllUsers,loginUser, getUserProfile, updateUser, deleteUser, createAdmin, removeAdmin, getAdmins} from "../controllers/user.js";
import {auth} from "../middlewares/authMiddleware.js"
import { addAccount } from "../controllers/accounts.js";

const router=express.Router();


/**
 * @swagger
 * /api/users/register:
 *   post:
 *     summary: Register a new user
 *     tags: [Users]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - name
 *               - email
 *               - password
 *             properties:
 *               name:
 *                 type: string
 *               email:
 *                 type: string
 *               password:
 *                 type: string
 *     responses:
 *       201:
 *         description: User registered successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                 message:
 *                   type: string
 *                 token:
 *                   type: string
 *       400:
 *         description: User already exists
 */
router.post('/register', registerUser);

/**
 * @swagger
 * /api/users/login:
 *   post:
 *     summary: Login user
 *     tags: [Users]
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - email
 *               - password
 *             properties:
 *               email:
 *                 type: string
 *               password:
 *                 type: string
 *     responses:
 *       200:
 *         description: Login successful
 *       400:
 *         description: Invalid credentials
 */
router.post('/login', loginUser);

router.get("/all",auth,getAllUsers)
/**
 * @swagger
 * /api/users/profile:
 *   get:
 *     summary: Get user profile
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     responses:
 *       200:
 *         description: User profile retrieved successfully
 *       401:
 *         description: Unauthorized
 */
router.get('/profile', auth, getUserProfile);
router.patch('/update_user', auth, updateUser);
// Delete user
router.delete('/delete_user', auth, deleteUser);
router.post('/add-account', auth, addAccount);


// Create admin (only for owner)
router.post('/create-admin', auth, createAdmin);

//  Remove admin
router.delete('/remove_admin/:adminId', auth, removeAdmin);
// Get all admins
router.get('/admins', auth, getAdmins);


export default router;